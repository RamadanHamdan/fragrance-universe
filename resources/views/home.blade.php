              <span class="absolute inset-0"></span>
              Getting to Know "Notes" in Perfume
            </a>
          </h3>
          <p class="mt-5 line-clamp-10 text-sm/6 text-gray-600">
            Top Notes: These are the first notes you smell when you spray a perfume. They are light and fresh, but they evaporate quickly, usually within 5-15 minutes. Examples include citrus (lemon, bergamot) or herbal (mint) notes.</p>

          <p class="mt-5 line-clamp-10 text-sm/6 text-gray-600">
            Middle Notes: These emerge after the top notes fade, and are the "heart" of the perfume. Their scent is richer and more complex, lasting about 20-60 minutes. Examples include floral (rose, jasmine) or spicy (cinnamon) notes.</p>

          <p class="mt-5 line-clamp-10 text-sm/6 text-gray-600">
            Base Notes: These are the foundation of the perfume, providing depth and longevity. Their aroma appears last and can last for hours. Examples include woody (sandalwood), amber, or musk notes.</p>
