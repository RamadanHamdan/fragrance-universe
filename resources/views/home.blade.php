          </h3>
          <p class="mt-5 line-clamp-10 text-sm/6 text-gray-600">
              Hot Weather (Daytime): In hot weather, scents tend to evaporate more easily. Choose a perfume with a fresh, light scent. Look for one with dominant notes like citrus, aquatic, or light floral. These fragrances give a cool, clean impression.</p>

          <p class="mt-5 line-clamp-10 text-sm/6 text-gray-600">
              Cool Weather (Nighttime/Rainy Season): In cold weather, scents don't diffuse as quickly as in hot weather, so you can use a stronger perfume. Choose one with a warm, rich, and intense scent. Perfumes with dominant notes of woody, amber, musk, or spicy are perfect choices.</p>
          <p class="mt-5 line-clamp-10 text-sm/6 text-gray-600">Perfume Glossary
              Eau de Parfum (EDP): Has a high concentration of fragrance oil (15-20%), so the fragrance lasts for about 4-8 hours.</p>
          <p class="mt-5 line-clamp-10 text-sm/6 text-gray-600">
              Eau de Toilette (EDT): Lower concentration of fragrance oil (5-15%), suitable for everyday use, lasting about 2-4 hours.<br>
              Silage: The trail of scent left behind after spraying perfume</p>
          <p class="mt-5 line-clamp-10 text-sm/6 text-gray-600">
              Longevity: The longevity of the perfume's scent on your skin.<br>
              Nose: The term for the perfumer.</p>
