          <p class="mt-5 line-clamp-10 text-sm/6 text-gray-600">History of Perfume
          The history of perfume began thousands of years ago, long before the modern bottles we know today. The word "perfume" itself comes from the Latin, per fumus, meaning "through smoke." Fragrances were first used in religious rituals by the ancient Egyptians, where they burned incense and spices as offerings to the gods.

